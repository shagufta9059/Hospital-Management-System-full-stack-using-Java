    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String did=dname.getText();
        
        try{
            DefaultTableModel model=(DefaultTableModel)table.getModel();
            //load driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            //create connection
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital","root","1133aA@5");
            String query="SELECT * FROM doctor WHERE id=?";
            PreparedStatement pstm=con.prepareStatement(query);
            pstm.setString(1,did);
            ResultSet rs=pstm.executeQuery();
            while(rs.next()){
                int ddid=rs.getInt("id");
                String pId=Integer.toString(ddid);
                String pnmae=rs.getString("name");
               String dpname=rs.getString("department"); 
               String row[] = {pId,pnmae,dpname};
               model.addRow(row);
            }
            con.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

